/*LA CONVOCATORIA Y LA EVALUACI?N CON LA QUE EL AUXILIAR QUEDO EN ESTADO SELECCIONADO "1" O NO SELECCIONADO "0"*/
CREATE OR REPLACE VIEW VISTA_SELECCION (CONVOCATORIA_ID, EVALUACION_AUXILIARES_ID, FECHA_CREACION_CONVOCATORIA, NOMBRE_AUXILIAR, APELLIDO_AUXILIAR, ESTADO)
AS SELECT CON.CONVOCATORIA_ID, EVA.EVALUACION_AUXILIARES_ID, CON.FECHA_CREACION , AUX.NOMBRE_AUXILIAR, AUX.APELLIDO_AUXILIAR, SEL.ESTADO
FROM SELECCION_AUXILIARES SEL, AUXILIAR AUX, CONVOCATORIA CON, EVALUACION_AUXILIARES EVA
WHERE AUX.AUXILIAR_ID= SEL.ID_AUXILIARES 
AND SEL.ID_EVALUACION_AUXILIARES= EVA.EVALUACION_AUXILIARES_ID
AND EVA.ID_CONVOCATORIA=CON.CONVOCATORIA_ID
ORDER BY CON.CONVOCATORIA_ID ASC;

/*PROMEDIO DE LA CALIFICACI?N DE LOS AUXILIARES SELECCIONADOS EN CADA UNA DE LAS EVALUACIONES */
CREATE OR REPLACE VIEW VISTA_PROMEDIO_EVALUACION(PROMEDIO_CALIFICACION, EVALUACION)
AS SELECT ROUND(AVG(SEL.CALIFICACION)) as PROMEDIO, EVA.EVALUACION_AUXILIARES_ID
FROM AUXILIAR AUX, SELECCION_AUXILIARES SEL, EVALUACION_AUXILIARES EVA 
WHERE SEL.ID_AUXILIARES= AUX.AUXILIAR_ID
AND SEL.ESTADO = 1
GROUP BY EVA.EVALUACION_AUXILIARES_ID;


/*PROMEDIO Y CALIFICACION DE EVALUACI?N DE LOS AUXILIARES SELECCIONADOS ORDENADOS DE MAYOR A MENOR  */
CREATE OR REPLACE VIEW VISTA_PROMEDIO_CALIFICACION(NOMBRE_AUXILIAR, APELLIDO_AUXILIAR, EVALUACION_AUXILIARES_ID, CALIFICACION, PROMEDIO)
AS SELECT AUX.NOMBRE_AUXILIAR, AUX.APELLIDO_AUXILIAR, SEL.ID_EVALUACION_AUXILIARES, SEL.CALIFICACION, SEL.PROMEDIO
FROM AUXILIAR AUX, SELECCION_AUXILIARES SEL
WHERE SEL.ID_AUXILIARES=AUX.AUXILIAR_ID
AND SEL.ESTADO=1
ORDER BY SEL.PROMEDIO DESC;

/*DATOS DEL SOLICITANTE CON LA DESCRIPCION DEL TIPO DE AUXILIAR, LA CANTIDAD DE HORAS Y AUXILIARES CON SU RESPECTIVO REQUISITO PARA ACCEDER A LA CONVOCATORIA PARA LA DICHA SOLICITUD*/
CREATE OR REPLACE VIEW VISTA_SOLICITUD_SOLICITANTE (NOMBRE_SOLICITANTE, APELLIDO_SOLICITANTE, CARGO, DESCRIPCION, CANTIDAD_AUXILIARES, CANTIDAD_HORAS, SOLICITUD, REQUISITO)
AS SELECT SOL.NOMBRE_SOLICITANTE, SOL.APELLIDO_SOLICITANTE, CAR.NOMBRE_CARGO, SOLA.DESC_SOLICITUD_AUXILIARES AS DESCRIPCION, SOLA.CANTIDAD_AUXILIARES, SOLA.CANTIDAD_HORAS, SOLAR.ID_SOLICITUD_AUXILIARES, REQ.DESCRIPCION_REQUISITO
FROM SOLICITANTE SOL, SOLICITUD_AUXILIARES SOLA, SOL_AUXILIARES_REQUISITOS SOLAR, REQUISITO REQ, CARGO CAR
WHERE SOLA.ID_SOLICITANTE=SOL.SOLICITANTE_ID
AND SOLA.SOLICITUD_AUXILIARES_ID=SOLAR.ID_SOLICITUD_AUXILIARES
AND SOL.ID_CARGO = CAR.CARGO_ID
AND REQ.REQUISITO_ID=SOLAR.ID_REQUISITO;

/*AUXILIAR Y LAS HORAS QUE EJECUTA*/
CREATE OR REPLACE VIEW VISTA_HORAS_AUXILIAR (NOMBRE_AUXILIAR, APELLIDO_AUXILIAR, TIPO, HORARIO, FECHA_HORARIO, HORA_INICIO, HORA_FIN)
AS SELECT AUX.NOMBRE_AUXILIAR, AUX.APELLIDO_AUXILIAR,  TIPO.DESCRIPCION_TIPO_AUXILIAR,  HORAC.HORARIO_ACTIVIDADES_ID, FEC.FECHA_FECHA_HORARIO, HORA.HORA_INICIO, HORA.HORA_FIN
FROM AUXILIAR AUX, HORARIO_ACTIVIDADES HORAC, TIPO_AUXILIAR TIPO, FECHA_HORARIO FEC, HORA HORA 
WHERE AUX.AUXILIAR_ID =HORAC.ID_AUXILIAR
AND HORAC.HORARIO_ACTIVIDADES_ID=FEC.ID_HORARIO_ACTIVIDADES
AND FEC.FECHA_HORARIO_ID= HORA.ID_FECHA_HORARIO
AND HORAC.ID_TIPO_AUXILIAR=TIPO.TIPO_AUXILIAR_ID;

/*AUXILIARES QUE CUMPLIERON CON LAS ACTIVIDADAES DE AUXILIAR*/
CREATE OR REPLACE VIEW VISTA_AUXILIARES_CUMPLIERON (NOMBRE_AUXILIAR, APELLIDO_AUXILIAR,DESCRIPCION)
AS SELECT AUX.NOMBRE_AUXILIAR, AUX.APELLIDO_AUXILIAR, CUM.DESC_CUMP_ACT AS DESCRIPCION
FROM CUMPLIMIENTO_ACTIVIDADES CUM, AUXILIAR AUX, AUX_CUMPLIMIENTO_ACTIVIDADES AUXCUM, SOLICITANTE SOL
WHERE AUX.AUXILIAR_ID = AUXCUM.ID_AUXILIAR
AND AUXCUM.ID_CUMPLIMIENTO_ACTIVIDADES=CUM.CUMP_ACT_ID
AND SOL.SOLICITANTE_ID = CUM.ID_SOLICITANTE;

/*MOSTRAR LOS SOLICITANTES Y LA CANTIDAD DE AUXILIARES QUE NECESITAN ORDENADO DE MAYOR A MENOR PARA DAR PRIORIDAD  */

CREATE OR REPLACE VIEW VISTA_CANTIDAD_AUXILIARES (ID_SOLICITANTE, NOMBRE_SOLICITANTE, CANTIDAD_AUXILIARES)
AS SELECT SOL.SOLICITANTE_ID AS "ID SOLICITANTE", SOL.NOMBRE_SOLICITANTE AS "NOMBRE DEL SOLICITANTE" , SUM(SOLA.CANTIDAD_AUXILIARES) AS "CANTIDAD DE AUXILIARES"
FROM SOLICITANTE SOL, SOLICITUD_AUXILIARES SOLA, SOL_AUXILIARES_REQUISITOS SOLAR
WHERE SOLA.ID_SOLICITANTE=SOL.SOLICITANTE_ID
AND SOLA.SOLICITUD_AUXILIARES_ID=SOLAR.ID_SOLICITUD_AUXILIARES
GROUP BY SOL.SOLICITANTE_ID, SOL.NOMBRE_SOLICITANTE
ORDER BY SUM(SOLA.CANTIDAD_AUXILIARES) DESC;


